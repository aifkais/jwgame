
public class Charakter {
	int id = 0;
	String name = "";
	int atk = 0;
	int def = 0;
	int crt = 0;
	int intl = 0;
	int lck = 0;
	int spd = 0;
	public Charakter(int id, String name, int atk, int def, int crt,int spd, int intl, int lck) {
		super();
		this.id = id;
		this.name = name;
		this.atk = atk;
		this.def = def;
		this.crt = crt;
		this.intl = intl;
		this.lck = lck;
	}
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the atk
	 */
	public int getAtk() {
		return atk;
	}
	/**
	 * @param atk the atk to set
	 */
	public void setAtk(int atk) {
		this.atk = atk;
	}
	/**
	 * @return the def
	 */
	public int getDef() {
		return def;
	}
	/**
	 * @param def the def to set
	 */
	public void setDef(int def) {
		this.def = def;
	}
	/**
	 * @return the spd
	 */
	public int getCrt() {
		return crt;
	}
	/**
	 * @param spd the spd to set
	 */
	public void setCrt(int spd) {
		this.crt = spd;
	}
	/**
	 * @return the intl
	 */
	public int getIntl() {
		return intl;
	}
	/**
	 * @param intl the intl to set
	 */
	public void setIntl(int intl) {
		this.intl = intl;
	}
	/**
	 * @return the lck
	 */
	public int getLck() {
		return lck;
	}
	/**
	 * @param lck the lck to set
	 */
	public void setLck(int lck) {
		this.lck = lck;
	}
	/**
	 * @return the spd
	 */
	public int getSpd() {
		return spd;
	}
	/**
	 * @param spd the spd to set
	 */
	public void setSpd(int spd) {
		this.spd = spd;
	}
	
	
	
}
